public class Main {

    public static void main(String[] args) {
	// for i++ 1 artır demek
        for (int i=1; i<=10; i++){
            System.out.println(i);
        }
        System.out.println ("Döngü bitti");
        // 10 a kadar olan tek sayıları yazdırmak için
        for (int i=1; i<=10; i+=2){
            System.out.println(i);
        }
    }
}
